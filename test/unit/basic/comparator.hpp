constexpr bool eq(int a, int b)
{
  return a == b;
}
constexpr bool neq(int a, int b)
{
  return a != b;
}
constexpr bool lt(int a, int b)
{
  return a < b;
}
constexpr bool gt(int a, int b)
{
  return a > b;
}
constexpr bool lte(int a, int b)
{
  return a <= b;
}
constexpr bool gte(int a, int b)
{
  return a >= b;
}
